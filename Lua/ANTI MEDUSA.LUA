local function setToggle(state)
    enabled = state
    status.Text = enabled and "ACTIVE" or "OFF"
    status.TextColor3 = enabled and Color3.fromRGB(40, 200, 100) or Color3.fromRGB(200, 60, 60)
    TweenService:Create(toggleBg, TweenInfo.new(0.2), {
        BackgroundColor3 = enabled and Color3.fromRGB(138, 43, 226) or Color3.fromRGB(45, 45, 58)
    }):Play()
    TweenService:Create(toggleKnob, TweenInfo.new(0.2), {
        Position = enabled and UDim2.new(1, -19, 0.5, -8) or UDim2.new(0, 3, 0.5, -8)
    }):Play()

    if enabled then
        if player.Character then setupChar(player.Character) end
    else
        stopAll()
    end
end

toggleBtn.MouseButton1Click:Connect(function()
    setToggle(not enabled)
end)

-- Draggable
local dragging, dragStart, startPos = false, nil, nil
main.InputBegan:Connect(function(inp)
    if inp.UserInputType == Enum.UserInputType.MouseButton1 or inp.UserInputType == Enum.UserInputType.Touch then
        dragging = true
        dragStart = inp.Position
        startPos = main.Position
        inp.Changed:Connect(function()
            if inp.UserInputState == Enum.UserInputState.End then dragging = false end
        end)
    end
end)
UserInputService.InputChanged:Connect(function(inp)
    if dragging and (inp.UserInputType == Enum.UserInputType.MouseMovement or inp.UserInputType == Enum.UserInputType.Touch) then
        local delta = inp.Position - dragStart
        main.Position = UDim2.new(startPos.X.Scale, startPos.X.Offset + delta.X, startPos.Y.Scale, startPos.Y.Offset + delta.Y)
    end
end)

-- ====================== INIT ======================
